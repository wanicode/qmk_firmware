// Runs whenever there is a layer state change.
uint32_t layer_state_set_user(uint32_t state) {
  uint8_t layer = biton32(state);
  switch (layer) {
      case _BASE:
        break;
      case _SYMB:
        break;
      case _NAVI:
        break;
      case _NUMP:
        break;
      case _FUNC:
        break;
      case _CONF:
        break;
      default:
        break;
    }

  return update_tri_layer_state(state, _MCRO, _FUNC, _CONF);
};